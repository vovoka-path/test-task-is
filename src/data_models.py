"""
Модуль с моделями данных для обработки документов.

Содержит определения dataclass для хранения фрагментов документов
и их метаданных.
"""

from dataclasses import dataclass
from typing import Dict


@dataclass
class Chunk:
    """
    Модель данных для хранения фрагмента документа с метаданными.
    
    Представляет собой отдельный чанк (фрагмент) документа с его
    текстовым содержимым и связанными метаданными.
    
    Attributes:
        content: Текстовое содержимое чанка
        metadata: Словарь с метаданными (контекст, источник и т.д.)
    """
    content: str
    metadata: Dict[str, str]