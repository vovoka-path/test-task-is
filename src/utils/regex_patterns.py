"""
Централизованное хранилище регулярных выражений для парсинга документов.
"""

import re

# Паттерн для разделения текста на блоки пунктов
CLAUSE_SPLIT_PATTERN = r'\n\s*(?=\d+\.\d+(\.\d+)*\s)'

# Паттерн для извлечения номера пункта
CLAUSE_NUMBER_PATTERN = r'^(\d+(?:\.\d+)*)'

# Паттерн для поиска ссылок на пункты
# Ищет ссылки в форматах: "пункт X.X", "пункта X.X", "подпункт X.X.X", "подпункта X.X.X"
# (\b(?:пункт|подпункт)[а-я]{0,2}\s+) - группа для захвата полного префикса ссылки
# (\d+(?:\.\d+)*) - группа для захвата номера пункта (например, 1.2 или 1.2.3)
CROSS_REFERENCE_PATTERN = re.compile(r'\b(?:пункт|подпункт)[а-я]{0,2}\s+(\d+(?:\.\d+)*)\b')

# Паттерн для извлечения заголовка документа
DOCUMENT_TITLE_PATTERN = r'^(ПРАВИЛА\s+№\s+\d+.*?)(?:\n|$)'

# Паттерн для извлечения короткого заголовка
SHORT_TITLE_PATTERN = r'(Правила\s+№\s+\d+)'
